#!/usr/bin/env node
// scripts/seed-prod-assets.mjs — copy local D1 assets straight into prod D1
// via `wrangler d1 execute --remote`, bypassing the HTTP layer (and CF Access).
//
// Default flow:
//   1. GET local /api/assets (so it captures UI edits, not just the markdown)
//   2. Remap user_email → prod identity (passed via --email)
//   3. Build a single SQL file: DELETE for that user + INSERT all rows
//   4. Pipe through `wrangler d1 execute setsushin-dash --remote --file=...`
//
// Idempotent — the DELETE clears the prod user's existing rows before insert,
// so re-running is safe. Use --dry-run to inspect the SQL without applying.
//
// Usage:
//   node scripts/seed-prod-assets.mjs --email me@example.com
//   node scripts/seed-prod-assets.mjs --email me@example.com --dry-run
//   node scripts/seed-prod-assets.mjs --email me@example.com --src-url http://localhost:8787

import { writeFile } from 'node:fs/promises';
import { spawn } from 'node:child_process';
import { parseArgs } from 'node:util';

const { values: args } = parseArgs({
  options: {
    // Required: the email CF Access provides for your prod identity.
    email:   { type: 'string' },
    'src-url': { type: 'string', default: 'http://127.0.0.1:8787' },
    'dry-run': { type: 'boolean', default: false },
  },
});

if (!args.email) {
  console.error('--email is required (CF Access prod identity)');
  process.exit(1);
}

const sqlEscape = (v) => {
  if (v == null) return 'NULL';
  if (typeof v === 'number') return String(v);
  // SQL string: wrap in single quotes, escape ' as ''
  return `'${String(v).replace(/'/g, "''")}'`;
};

async function main() {
  const r = await fetch(`${args['src-url']}/api/assets`);
  if (!r.ok) throw new Error(`local fetch failed: ${r.status} (is wrangler dev up?)`);
  const items = await r.json();
  if (!Array.isArray(items) || items.length === 0) {
    throw new Error('local /api/assets returned 0 rows — nothing to seed');
  }
  console.log(`source: ${args['src-url']}/api/assets → ${items.length} rows`);
  console.log(`target: prod D1, user_email = ${args.email}`);

  const lines = [
    `-- Generated by scripts/seed-prod-assets.mjs at ${new Date().toISOString()}`,
    `-- Wipes assets for ${args.email} and reinserts ${items.length} rows from local D1.`,
    `DELETE FROM assets WHERE user_email = ${sqlEscape(args.email)};`,
  ];
  for (const it of items) {
    lines.push(
      `INSERT INTO assets (user_email, layer, sublayer, name, jpy_man, exposure, account, sort_order) ` +
      `VALUES (${sqlEscape(args.email)}, ${sqlEscape(it.layer)}, ${sqlEscape(it.sublayer)}, ` +
      `${sqlEscape(it.name)}, ${sqlEscape(it.jpy_man)}, ${sqlEscape(it.exposure)}, ` +
      `${sqlEscape(it.account)}, ${sqlEscape(it.sort_order ?? 0)});`
    );
  }
  const sql = lines.join('\n') + '\n';

  if (args['dry-run']) {
    process.stdout.write(sql);
    console.error('\n(dry-run — not applied)');
    return;
  }

  const sqlPath = '/tmp/seed-prod-assets.sql';
  await writeFile(sqlPath, sql);
  console.log(`wrote ${sqlPath} (${lines.length - 3} INSERTs + 1 DELETE)`);

  await new Promise((resolve, reject) => {
    const p = spawn('npx', [
      'wrangler@latest', 'd1', 'execute', 'setsushin-dash',
      '--remote', '--file', sqlPath,
    ], { stdio: 'inherit' });
    p.on('exit', (code) => code === 0 ? resolve() : reject(new Error(`wrangler exit ${code}`)));
  });

  console.log(`\n✓ done. Refresh your dashboard URL to see the assets.`);
}

main().catch((e) => { console.error(e); process.exit(1); });
